package io.nanovc.meh;

/**
 * Concepts used to define the Model-Engine-Handler unification of architectural patterns.
 */
public enum MEHConcepts
{
    MODEL,
    ENGINE,
    HANDLER,
    CONTROLLER,

    CLASS,
    CONCEPT,

    FIELDS,
    STATE,

    RECORDS,
    STRUCTURE,

    FUNCTIONS,
    METHODS,
    BEHAVIOUR,

    ARGUMENTS,

    VARIABLES,
    DATA,
    CONTEXT,


    PATTERNS,

    /**
     * <pre>
     *
     +----------------------------------------------------------------+
     |                             {@link #OOP}                                |
     |  +----------------------------------------------------------+  |
     |  |  {@link #CLASS} - {@link #CONCEPT}                                         |  |
     |  |                                                          |  |
     |  |    +------------------+      +-----------------------+   |  |
     |  |    |  {@link #FIELDS} - {@link #STATE}  +----->+  {@link #METHODS} - {@link #BEHAVIOUR}  |   |  |
     |  |    +------------------+      +-----------^-----------+   |  |
     |  |                                          |               |  |
     |  +------------------------------------------+---------------+  |
     |                                             |                  |
     |  +---------------------------------------+  |                  |
     |  | {@link #ARGUMENTS}                             |  |                  |
     |  | +--------------+  +-----------------+ |  |                  |
     |  | |{@link #VARIABLES}-{@link #DATA}|  |{@link #VARIABLES}-{@link #CONTEXT}| +--|                  |
     |  | +--------------+  +-----------------+ |                     |
     |  +---------------------------------------+                     |
     |                                                                |
     +----------------------------------------------------------------+
     * </pre>
     * @see MEHPatterns#OOP
     */
    OOP,

    /**
     *
     * <pre>
     *
     +-------------------------------------------------------------+
     |                        {@link #MODEL_ENGINE}                         |
     |                                                             |
     |  {@link #MODEL}:                      {@link #ENGINE}:                        |
     |  +----------------------+    +---------------------------+  |
     |  | {@link #RECORDS} - {@link #STRUCTURE}  |    |  {@link #FUNCTIONS} - {@link #BEHAVIOUR}  | |  |
     |  | +------------------+ |    | +-----------------------+ |  |
     |  | |  {@link #FIELDS} - {@link #STATE}  +------->+  {@link #METHODS} - {@link #BEHAVIOUR}  | |  |
     |  | +------------------+ |    | +------------^----------+ |  |
     |  +----------------------+    +--------------|------------+  |
     |                                             |               |
     |  +---------------------------------------+  |               |
     |  | {@link #ARGUMENTS}                             |  |               |
     |  | +--------------+  +-----------------+ |  |               |
     |  | |{@link #VARIABLES}-{@link #DATA}|  |{@link #VARIABLES}-{@link #CONTEXT}| +--|               |
     |  | +--------------+  +-----------------+ |                  |
     |  +---------------------------------------+                  |
     |                                                             |
     +-------------------------------------------------------------+
     * </pre>
     * @see MEHPatterns#MODEL_ENGINE
     */
    MODEL_ENGINE,

    /**
     *
     * <pre>
     *
     +-------------------------------------------------------------------+
     |                   {@link #MODEL_ENGINE_HANDLER}                            |
     |  {@link #HANDLER}:                                                         |
     |  +-------------------------------------------------------------+  |
     |  |                                                             |  |
     |  |  {@link #MODEL}:                      {@link #ENGINE}:                        |  |
     |  |  +----------------------+    +---------------------------+  |  |
     |  |  | {@link #RECORDS} - {@link #STRUCTURE}  |    |  {@link #FUNCTIONS} - {@link #BEHAVIOUR}  | |  |  |
     |  |  | +------------------+ |    | +-----------------------+ |  |  |
     |  |  | |  {@link #FIELDS} - {@link #STATE}  +------->+  {@link #METHODS} - {@link #BEHAVIOUR}  | |  |  |
     |  |  | +------------------+ |    | +-------^-----^---------+ |  |  |
     |  |  +----------------------+    +---------|-----|-----------+  |  |
     |  |                                        |     |              |  |
     |  |                   {@link #ARGUMENTS}:           |     |              |  |
     |  |                   +-----------------+  |     |              |  |
     |  |                   |{@link #VARIABLES}-{@link #CONTEXT}+--|     |              |  |
     |  |                   +-----------------+        |              |  |
     |  +----------------------------------------------|--------------+  |
     |                                                 |                 |
     |    {@link #ARGUMENTS}:                                   |                 |
     |    +--------------+                             |                 |
     |    |{@link #VARIABLES}-{@link #DATA}+-----------------------------|                 |
     |    +--------------+                                               |
     |                                                                   |
     +-------------------------------------------------------------------+
     * </pre>
     * @see MEHPatterns#MODEL_ENGINE_HANDLER
     */
    MODEL_ENGINE_HANDLER,

    /**
     *
     * <pre>
     *
     +-------------------------------------------------------------------+
     |                       {@link #MODEL_CONTROLLER}                            |
     |  {@link #CONTROLLER}:                                                      |
     |  +-------------------------------------------------------------+  |
     |  |                                                             |  |
     |  |  {@link #MODEL}:                                                     |  |
     |  |  +----------------------+                                   |  |
     |  |  | {@link #RECORDS} - {@link #STRUCTURE}  |                                   |  |
     |  |  | +------------------+ |      +-----------------------+    |  |
     |  |  | |  {@link #FIELDS} - {@link #STATE}  +------->+  {@link #METHODS} - {@link #BEHAVIOUR}  |    |  |
     |  |  | +------------------+ |      +-------^-----^---------+    |  |
     |  |  +----------------------+              |     |              |  |
     |  |                                        |     |              |  |
     |  |                   {@link #ARGUMENTS}:           |     |              |  |
     |  |                   +-----------------+  |     |              |  |
     |  |                   |{@link #VARIABLES}-{@link #CONTEXT}+--|     |              |  |
     |  |                   +-----------------+        |              |  |
     |  +----------------------------------------------|--------------+  |
     |                                                 |                 |
     |    {@link #ARGUMENTS}:                                   |                 |
     |    +--------------+                             |                 |
     |    |{@link #VARIABLES}-{@link #DATA}+-----------------------------|                 |
     |    +--------------+                                               |
     |                                                                   |
     +-------------------------------------------------------------------+
     * </pre>
     * @see MEHPatterns#MODEL_CONTROLLER
     */
    MODEL_CONTROLLER,
}
